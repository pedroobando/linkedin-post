/**
 * Convert entity name to PascalCase for types
 */
export function toPascalCase(str: string): string {
  return str
    .replace(/[-_]/g, ' ')
    .replace(/(?:^\w|[A-Z]|\b\w)/g, (word) => word.toUpperCase())
    .replace(/\s+/g, '');
}

/**
 * Convert entity name to camelCase for variables
 */
export function toCamelCase(str: string): string {
  const pascal = toPascalCase(str);
  return pascal.charAt(0).toLowerCase() + pascal.slice(1);
}

/**
 * Get the TypeScript type for a schema field
 */
export function getTypeScriptType(field: SchemaField): string {
  switch (field.type) {
    case 'text':
    case 'enum':
    case 'fk':
      return 'string';
    case 'integer':
      return 'number';
    case 'real':
      return 'number';
    case 'boolean':
      return 'boolean';
    case 'timestamp':
      return 'Date';
    default:
      return 'unknown';
  }
}

/**
 * Get editable fields (exclude auto-generated and timestamps)
 */
export function getEditableFields(schema: EntitySchema): SchemaField[] {
  return schema.fields.filter(f => 
    !f.isAutoGenerated && 
    !f.isTimestamp &&
    !f.isPrimaryKey
  );
}

/**
 * Get visible fields (for table columns)
 */
export function getVisibleFields(schema: EntitySchema): SchemaField[] {
  return schema.fields.filter(f => 
    !f.isPrimaryKey || f.name !== 'id' // Usually hide ID column
  );
}

/**
 * Get form fields (editable + some timestamps for display)
 */
export function getFormFields(schema: EntitySchema): SchemaField[] {
  return getEditableFields(schema);
}

/**
 * Generate Zod schema for a field
 */
export function generateZodValidation(field: SchemaField): string {
  const baseSchema = (() => {
    switch (field.type) {
      case 'text':
      case 'fk':
        return 'z.string()';
      case 'integer':
        return 'z.number().int()';
      case 'real':
        return 'z.number()';
      case 'boolean':
        return 'z.boolean()';
      case 'enum':
        if (field.enumValues && field.enumValues.length > 0) {
          return `z.enum([${field.enumValues.map(v => `'${v}'`).join(', ')}])`;
        }
        return 'z.string()';
      default:
        return 'z.any()';
    }
  })();
  
  if (field.isRequired) {
    if (field.type === 'text') {
      return `${baseSchema}.min(1, 'Este campo es requerido')`;
    }
    return baseSchema;
  }
  
  return `${baseSchema}.optional()`;
}

// ============================================================================
// Main Export
// ============================================================================

export interface AnalyzerOptions {
  schemaDir?: string;
  schemaFile?: string;
}

/**
 * Main function to analyze an entity schema
 */
export function analyzeEntity(
  entityName: string, 
  options: AnalyzerOptions = {}
): EntitySchema {
  const schemaDir = options.schemaDir || 'src/db/schema';
  
  // Try to find the schema file
  const possiblePaths = [
    path.join(schemaDir, `${entityName}.ts`),
    path.join(schemaDir, 'index.ts'),
    path.join(schemaDir, `${toCamelCase(entityName)}.ts`),
  ];
  
  if (options.schemaFile) {
    possiblePaths.unshift(options.schemaFile);
  }
  
  for (const schemaPath of possiblePaths) {
    if (fs.existsSync(schemaPath)) {
      return analyzeSchema(schemaPath, entityName);
    }
  }
  
  throw new Error(`Could not find schema file for entity ${entityName}`);
}

export default {
  analyzeEntity,
  analyzeSchema,
  toPascalCase,
  toCamelCase,
  getTypeScriptType,
  getEditableFields,
  getVisibleFields,
  generateZodValidation,
};
